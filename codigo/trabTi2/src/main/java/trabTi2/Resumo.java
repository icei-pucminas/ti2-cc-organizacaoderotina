package trabTi2;

public class Resumo {

	private String nome;
	private String materia;
	private String conteudo;
	
	public Resumo() {
		
		nome = "";
		materia = "";
		conteudo = "";
	}
	
	public Resumo(String nome, String mat, String cont) {
		
		this.nome = nome;
		this.materia = mat;
		this.conteudo = cont;
	}
}
